public class Player{
   
    String playerName;
    int playerCoin;

    public void setName(String name) {
        this.playerName = name;
    }   
    public String getName() {
        return playerName;
    }
    public void setPCoins(int coin) {
        this.playerCoin = coin;
    }
    public int getPCoin() {
        return playerCoin;
    }

}